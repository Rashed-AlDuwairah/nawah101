/// Nawah Design System — Spacing & Radius Tokens
///
/// Consistent spacing and border radius values used across the app.
abstract final class AppSpacing {
  // ── Spacing ──
  static const double xs = 4;
  static const double sm = 8;
  static const double md = 12;
  static const double lg = 16;
  static const double xl = 20;
  static const double xxl = 24;
  static const double xxxl = 32;

  // ── Page Padding ──
  static const double pagePaddingH = 20;
  static const double pagePaddingV = 16;

  // ── Section Spacing ──
  static const double sectionGap = 24;
  static const double cardPadding = 16;
}

/// Nawah Design System — Border Radius Tokens
abstract final class AppRadius {
  static const double sm = 8;
  static const double md = 12;
  static const double lg = 16;
  static const double xl = 24;
  static const double pill = 100;
}
